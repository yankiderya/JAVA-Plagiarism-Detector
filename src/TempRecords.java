/*All calculated similarity percentages and current sub-texts are temporarily kept in this class. */
public class TempRecords {
    private Long percentage;
    private int s_sentence;


    public Long getPercentage() {
        return percentage;
    }

    public void setPercentage(Long percentage) {
        this.percentage = percentage;
    }

    public int getS_sentence() {
        return s_sentence;
    }

    public void setS_sentence(int s_sentence) {
        this.s_sentence = s_sentence;
    }

}
